    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel d1 =(DefaultTableModel)TableA.getModel();
        int selectIndex = TableA.getSelectedRow();
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

        String AName = txtAName.getText();
        String AAddress = txtAAddress.getText();
        String PNo = txtAPno.getText();
        
        try {
            pst = con.prepareCall("update author set Author = ?, Address = ?, PhoneNo = ? where AID = ?");
            pst.setString(1, AName);
            pst.setString(2, AAddress);
            pst.setString(3, PNo);
            pst.setInt(4, id);
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"Author updated.");
                txtAName.setText("");
                txtAAddress.setText("");
                txtAPno.setText("");
                txtAName.requestFocus();
                jButton1.setEnabled(true);
                LoadAuthor();

            }
            else{
                JOptionPane.showMessageDialog(this,"Error.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Author.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
