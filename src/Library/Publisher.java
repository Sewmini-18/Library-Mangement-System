    public void LoadPub(){
        int c;
        try {
            pst = con.prepareStatement("select * from publisher");
            res = pst.executeQuery();
            
            ResultSetMetaData rsd = res.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d =(DefaultTableModel)TableP.getModel();
            d.setRowCount(0);
            
            while(res.next()){
                Vector v2 = new Vector(); 
                
                for(int i=1; i<=c; i++){
                    v2.add(res.getString("PID"));
                    v2.add(res.getString("Publisher"));
                    v2.add(res.getString("Address"));
                    v2.add(res.getString("PhoneNo"));
                }
                
                d.addRow(v2);
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(Publisher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
