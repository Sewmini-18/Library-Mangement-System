    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel d1 =(DefaultTableModel)TableP.getModel();
        int selectIndex = TableP.getSelectedRow();
        int id = Integer.parseInt(d1.getValueAt(selectIndex, 0).toString());

        String PName = txtPName.getText();
        String PAddress = txtPAddress.getText();
        String PPno = txtPPno.getText();
       
        try {
            pst = con.prepareCall("update publisher set Publisher = ?, Address = ?, PhoneNO = ? where PID = ?");
            pst.setString(1, PName);
            pst.setString(2, PAddress);
            pst.setString(3, PPno);
            pst.setInt(4, id);
            
            int k = pst.executeUpdate();
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"Publisher updated.");
                txtPName.setText("");
                txtPAddress.setText("");
                txtPPno.setText("");
                txtPName.requestFocus();
                LoadPub();
                jButton1.setEnabled(true);

            }
            else{
                JOptionPane.showMessageDialog(this,"Error.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Publisher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
