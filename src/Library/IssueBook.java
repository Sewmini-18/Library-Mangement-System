    private void txtmidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtmidKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyCode() == KeyEvent.VK_ENTER)
        {
               String mid = txtmid.getText();
               
            try {
                pst = con.prepareStatement("select * from member where MID = ?");
                pst.setString(1, mid);
                res = pst.executeQuery();
                
                if(res.next() == false){
                    JOptionPane.showMessageDialog(this, "Member ID not found");
                }
                
                else{
                    String mName = res.getString("Member");
                    txtmname.setText(mName.trim());
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
            }
               
        }
    }//GEN-LAST:event_txtmidKeyPressed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:Add

        String mid = txtmid.getText();
        bookItem bitem = (bookItem) txtbname.getSelectedItem();
        
        SimpleDateFormat date_format = new SimpleDateFormat("yyyy-MM-dd");
        String IssueDate = date_format.format(txtdate.getDateFormat());
        
        SimpleDateFormat date_format1 = new SimpleDateFormat("yyyy-MM-dd");
        String ReturnDate = date_format.format(txtrdate.getDateFormat());
        
        
                
        try {
            pst = con.prepareStatement("insert into issuedate(MemberID,BookID,IssueDate,ReturnDate) values(?,?,?,?)");
            pst.setString(1, mid);
            pst.setInt(2, bitem.id);
            pst.setString(3, IssueDate);
            pst.setString(4, ReturnDate);
            int k = pst.executeUpdate();
            
            
            
            if(k==1){
                JOptionPane.showMessageDialog(this,"~Book Issued...~");
                txtmid.setText("");
                txtbname.setSelectedIndex(-1);
                txtmname.setText("");
                txtmid.requestFocus();
                LoadIssueBook();

            }
            else{
                JOptionPane.showMessageDialog(this,"Error.");
            }

        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

