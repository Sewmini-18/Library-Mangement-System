     public void Book(){
        
        try {
            pst = con.prepareStatement("select * from book");
            res = pst.executeQuery();
            txtbname.removeAllItems();
            
            while(res.next()){
                txtbname.addItem(new bookItem(res.getInt(1), res.getString(2)));
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Book.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    public void LoadIssueBook(){
        int c;
        try {
            pst = con.prepareStatement("select i.ID, m.member, b.Book, i.IssueDate, i.ReturnDate from issuebook i JOIN member m On i.MemberID = m.MID JOIN book b On i.BookID =b.BID ");
            res = pst.executeQuery();
            
            ResultSetMetaData rsd = res.getMetaData();
            c = rsd.getColumnCount();
            DefaultTableModel d =(DefaultTableModel)TableIBook.getModel();
            d.setRowCount(0);
            
            while(res.next()){
                Vector v2 = new Vector(); 
                
                for(int i=1; i<=c; i++){
                    v2.add(res.getString("i.ID"));
                    v2.add(res.getString("m.member"));
                    v2.add(res.getString("b.Book"));
                    v2.add(res.getString("i.IssueDate"));
                    v2.add(res.getString("i.ReturnDate"));


                }
                
                d.addRow(v2);
            }
             
        } catch (SQLException ex) {
            Logger.getLogger(IssueBook.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
